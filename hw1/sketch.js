// 호수 위의 기하학 정원 – 함수 없이 연꽃/물결을 직접 배치한 버전
// 사용 도형: rect, square, triangle, circle, ellipse, line, point, quad

function setup() {
  createCanvas(600, 600);
  noLoop();
}

function draw() {
  // 1) 배경(하늘 레이어)
  background(200, 230, 255);
  

  // 2) 태양(타원 헤일로 + 원)
  noStroke();
  fill(255, 200, 100, 120);
  ellipse(450, 130, 180, 120);
  fill(255, 180, 70);
  circle(450, 130, 90);

  // 3) 먼 산(삼각형들)
  noStroke();
  fill(80, 120, 140);
  triangle(40, 320, 180, 180, 320, 320);
  fill(60, 100, 120);
  triangle(200, 340, 360, 200, 520, 340);
  fill(90, 130, 150);
  triangle(380, 330, 500, 220, 600, 330);

  // 4) 수면(직사각형)
  fill(150, 190, 210, 220);
  rect(0, 320, width, 280);

  // 5) 수면 위 플랫폼(사각형/정사각형)
  fill(210, 230, 240, 230); square(60, 360, 80);
  fill(200, 220, 235, 230); rect(160, 380, 110, 70);
  fill(220, 235, 245, 230); rect(310, 350, 90, 50);
  fill(205, 225, 240, 230); square(430, 390, 70);

  // 6) 연꽃들 — 함수 없이 직접 그리기 (잎사귀 타원 + 꽃잎 타원 + 중심 원)
  noStroke();
  // 꽃 1 (cx=115, cy=400)
  fill(70, 150, 120, 220); ellipse(115, 414, 60, 18); // 잎
  fill(255, 160, 170);     ellipse(115, 400, 30, 18); // 꽃잎들
  ellipse(105, 402, 24, 14);
  ellipse(125, 402, 24, 14);
  ellipse(115, 397, 22, 12);
  fill(255, 210, 220);     circle(115, 400, 8);       // 중심

  // 꽃 2 (cx=215, cy=420)
  fill(70, 150, 120, 220); ellipse(215, 434, 60, 18);
  fill(240, 150, 190);     ellipse(215, 420, 30, 18);
  ellipse(205, 422, 24, 14);
  ellipse(225, 422, 24, 14);
  ellipse(215, 417, 22, 12);
  fill(255, 225, 235);     circle(215, 420, 8);

  // 꽃 3 (cx=355, cy=410)
  fill(70, 150, 120, 220); ellipse(355, 424, 60, 18);
  fill(250, 170, 150);     ellipse(355, 410, 30, 18);
  ellipse(345, 412, 24, 14);
  ellipse(365, 412, 24, 14);
  ellipse(355, 407, 22, 12);
  fill(255, 225, 210);     circle(355, 410, 8);

  // 꽃 4 (cx=470, cy=430)
  fill(70, 150, 120, 220); ellipse(470, 444, 60, 18);
  fill(230, 145, 200);     ellipse(470, 430, 30, 18);
  ellipse(460, 432, 24, 14);
  ellipse(480, 432, 24, 14);
  ellipse(470, 427, 22, 12);
  fill(245, 210, 250);     circle(470, 430, 8);

  // 7) 줄기(선) — 함수 없이 직접
  stroke(40, 110, 120, 180);
  strokeWeight(2);
  line(115, 400, 115, 470);
  line(215, 420, 215, 480);
  line(355, 410, 355, 490);
  line(470, 430, 470, 500);

  // 8) 물결 — wave() 없이 선분을 여러 줄 직접
  stroke(255, 255, 255, 120);
  strokeWeight(1);
  // 물결 A (y=520 주변)
  line(80, 520, 260, 520);
  line(80, 524, 260, 524);
  line(80, 528, 260, 528);
  line(80, 532, 260, 532);
  line(80, 536, 260, 536);
  line(80, 540, 260, 540);
  // 물결 B (y=540 주변)
  line(260, 540, 460, 540);
  line(260, 544, 460, 544);
  line(260, 548, 460, 548);
  line(260, 552, 460, 552);
  line(260, 556, 460, 556);
  line(260, 560, 460, 560);
  // 물결 C (y=520 주변, 오른쪽)
  line(420, 520, 570, 520);
  line(420, 524, 570, 524);
  line(420, 528, 570, 528);
  line(420, 532, 570, 532);
  line(420, 536, 570, 536);
  line(420, 540, 570, 540);

}